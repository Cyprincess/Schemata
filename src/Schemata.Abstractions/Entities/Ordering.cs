namespace Schemata.Abstractions.Entities;

/// <summary>
///     Sort direction for ordered queries.
/// </summary>
public enum Ordering
{
    /// <summary>
    ///     Sort in ascending order (smallest to largest).
    /// </summary>
    Ascending,

    /// <summary>
    ///     Sort in descending order (largest to smallest).
    /// </summary>
    Descending,
}
